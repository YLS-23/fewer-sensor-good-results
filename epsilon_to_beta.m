    case 0.2 % 第6列
        beta = interp1(e_tbl,B(:,6),epsilon,"linear");
    case 1/6 | 0.1667 % 第7列
        beta = interp1(e_tbl,B(:,7),epsilon,"linear");
    case 0.125 % 第8列
        beta = interp1(e_tbl,B(:,8),epsilon,"linear");
end

end