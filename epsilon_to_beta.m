switch bd
    case 1 % 第1列
        beta = interp1(e_tbl,B(:,1),epsilon,"linear");
    case 0.5 % 第2列
        beta = interp1(e_tbl,B(:,2),epsilon,"linear");
    case 0.4 % 第3列
        beta = interp1(e_tbl,B(:,3),epsilon,"linear");
    case 1/3 | 0.3333 % 第4列
